    public void cadastrar(){
        conexao = Conexao.obterConexao();
        
        try{
            String sql = "insert into cliente (nome, telefone, endereco, email) values (?,?,?,?)";
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString ( 2,txtTelefone.getText () );
            pst.setString ( 3,txtEndereco.getText () );
            pst.setString (4,txtEmail. getText () ) ;
            
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "erro ao fazer cadastro");
        }
        limpar();
         
    }
    
   public void listar(){
            conexao = Conexao.obterConexao();
            DefaultTableModel model = (DefaultTableModel)tblCli.getModel();
            model.setNumRows(0);
            try{
            String sql = "select * from cliente where nome like ?";
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtPesquisa.getText () + "%");
            rs = pst.executeQuery();
            while (rs.next () ) {
            model.addRow (new Object [] {
            rs.getInt("codigo"),
            rs.getString ("nome") ,
            rs.getString ("telefone"),
            rs.getString ("endereco"),
            rs.getString ("email")
               });
            }//fim while
            pst.close ();
            }//fim try
            catch (Exception e) {
            }//fim catch
            txtPesquisa.setText("");
            txtPesquisa.grabFocus();
            }//fim listar
   
            
   

